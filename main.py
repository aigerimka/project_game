class Control:

    def __init__(self):
        self.game = True
        self.direction = 'RIGHT'
        self.pause = True

    def control(self):
        for i in pygame.event.get():
            if i.type == pygame.QUIT:
                self.game = False
            elif i.type == pygame.KEYDOWN:
                if i.key == pygame.K_RIGHT and self.direction != 'LEFT':
                    self.direction = 'RIGHT'
                elif i.key == pygame.K_LEFT and self.direction != 'RIGHT':
                    self.direction = 'LEFT'
                elif i.key == pygame.K_UP and self.direction != 'DOWN':
                    self.direction = 'UP'
                elif i.key == pygame.K_DOWN and self.direction != 'UP':
                    self.direction = 'DOWN'
                elif i.key == pygame.K_ESCAPE:
                    self.game = False
                elif i.key == pygame.K_SPACE:
                    if self.pause:
                        self.pause = False
                        pygame.mixer.music.pause()
                    elif not self.pause:
                        self.pause = True
                        pygame.mixer.music.unpause()


class Snake:

    def __init__(self, score=0):
        self.game = True
        self.head = [45, 45]
        self.body = [[45, 45], [34, 45], [23, 45]]
        self.font = pygame.font.SysFont('Comic Sans MS', 30)
        self.score = score

    def move(self, control):
        if control.direction == 'RIGHT':
            self.head[0] += 11
        elif control.direction == 'LEFT':
            self.head[0] -= 11
        elif control.direction == 'UP':
            self.head[1] -= 11
        elif control.direction == 'DOWN':
            self.head[1] += 11

    def draw(self, window):
        for segment in self.body:
            pygame.draw.circle(window, pygame.Color('green'), [segment[0], segment[1]], 6)

    def animation(self):
        self.body.insert(0, list(self.head))
        self.body.pop()

    def eat_apple(self, food, gui):
        if self.head == food.apple_position:
            self.body.append(food.apple_position)
            food.get_apple_position(gui)
            self.score += 1

    def die(self, gui):
        if self.head in gui.barriers or self.head in self.body[1:]:
            self.game = False
            self.text = self.font.render('GAME OVER', True, pygame.Color('red'))
            window.blit(self.text, (120, 100))
            self.show_score()

    def show_score(self):
        self.text = self.font.render('YOUR SCORE: ' + str(self.score), True, pygame.Color('red'))
        window.blit(self.text, (100, 200))


