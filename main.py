class Apples:

    def __init__(self):
        self.apple_position = []

    def get_apple_position(self, gui):
        self.apple_position = random.choice(gui.game_field)

    def draw_apple(self, window):
        pygame.draw.circle(window, pygame.Color('red'), [self.apple_position[0], self.apple_position[1]], 6)


def main():
    control = Control()
    snake = Snake()
    gui = Gui()
    food = Apples()
    gui.field()
    food.get_apple_position(gui)
    speed = 0

    while control.game and snake.game:
        control.control()
        window.fill(pygame.Color('black'))
        snake.draw(window)
        food.draw_apple(window)
        gui.draw(window)
        if speed % 50 == 0 and control.pause:
            snake.move(control)
            snake.die(gui)
            snake.eat_apple(food, gui)
            snake.animation()
        speed += 1
        pygame.display.flip()
        if not snake.game:
            time.sleep(3)


if __name__ == '__main__':
    main()
